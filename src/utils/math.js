/**
 * Adds two numbers.
 *
 * @param {number} a A number to add to.
 * @param {number} b A number to add to `a`.
 */
export function add(a, b) {
  return a + b
}

/**
 * Subtracts a value from another value.
 *
 * @param {number} a A number to subtract from
 * @param {number} b A number to subtract from `a`
 */
export function subtract(a, b) {
  return a - b
}
